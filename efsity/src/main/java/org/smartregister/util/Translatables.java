package org.smartregister.util;

import java.util.List;

public class Translatables {
  private List<String> questionnaireTranslatables;
  private List<String> configTranslatables;

  // Getters and Setters

  public List<String> getQuestionnaireTranslatables() {
    return questionnaireTranslatables;
  }

  public void setQuestionnaireTranslatables(List<String> questionnaireTranslatables) {
    this.questionnaireTranslatables = questionnaireTranslatables;
  }

  public List<String> getConfigTranslatables() {
    return configTranslatables;
  }

  public void setConfigTranslatables(List<String> configTranslatables) {
    this.configTranslatables = configTranslatables;
  }
}
